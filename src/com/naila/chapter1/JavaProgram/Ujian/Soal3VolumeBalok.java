package com.naila.chapter1.JavaProgram.Ujian;

public class Soal3VolumeBalok {
    public static void main(String[] args) {
        double p = 5 ;
        double l = 6 ;
        double t = 10 ;
        double v = p * l * t ;
        System.out.print("Volume balok dengan panjang "  + p +
                ", lebar "  + l +  " dan tinggi "  + t +  " adalah "  + v +  " cm3") ;

    }
}

// Volume balok dengan panjang 5.0, lebar 6.0 dan tinggi 10.0 adalah 300.0 cm3